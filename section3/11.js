console.log("11- Exercise - Swapping Variables");
// -----------------------------------

let a = "red";
let b = "blue";

console.log("before swapping");
console.log("a", a);
console.log("b", b);
// -----------------------------------

let temp = a;
a = b;
b = temp;

console.log("after swapping");
console.log("a", a);
console.log("b", b);
