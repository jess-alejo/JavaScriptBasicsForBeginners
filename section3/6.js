// If a person is under 18 years old
// that person is a minor
// otherwise, that person is an adult

let age = 17;
let type = age < 18 ? "Minor" : "Adult";
// age < 18
// - a condition to evaluate, is age less than 18?
// ? "Minor"
// - the result if the condition is true
// : "Adult"
// - the result if the condition is false

console.log(type);
