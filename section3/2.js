console.log("3.2- Arithmetic Operators");
// -----------------------------------

let x = 15;
let y = 4;

console.log("x", x);
console.log("y", y);

// expression is something that produces a value
console.log("addition", "x + y =", x + y);
console.log("subtraction", "x - y =", x - y);
console.log("multiplication", "x * y =", x * y);
console.log("division", "x / y =", x / y);
console.log("modulo", "x % y =", x % y); // remainder of division

// new in ES6
console.log("exponentiation", "x ** y =", x ** y); // x to the power of y

// increment/decrement
console.log("increment", "++x", ++x);
// note position of ++ is important
// ++x will increment x by 1 and perform action
// x++ will perform action to x then increment by 1

console.log("decrement", "--y", --y);
